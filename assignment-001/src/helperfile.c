int AddNums(int iNum1, int iNum2)
{
  return (iNum1 + iNum2);
}

int SubNums(int iNum1, int iNum2)
{
  return (iNum1 - iNum2);
}

int MultNums(int iNum1, int iNum2)
{
  return (iNum1 * iNum2);
}

float DivNums(int iNum1, int iNum2)
{
  return (iNum1 / (float)iNum2);
}
